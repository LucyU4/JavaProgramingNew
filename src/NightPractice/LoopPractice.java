package NightPractice;

public class LoopPractice {

        /*
        While Loop Practice

write a program that will add all the numbers from 1 - 500

         */
        public static void main(String[] args) {

            int count = 0;
            int sum = 500;
            do{
                System.out.print(count + " ");

                count++;
            }
            while (count< sum); //1,2,3,4,5----500




            }

            }


